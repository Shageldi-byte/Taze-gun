package com.shageldi.tazegun;

public class Summa {
    String olace;
    String summa;

    public Summa(String olace, String summa) {
        this.olace = olace;
        this.summa = summa;
    }

    public String getOlace() {
        return olace;
    }

    public void setOlace(String olace) {
        this.olace = olace;
    }

    public String getSumma() {
        return summa;
    }

    public void setSumma(String summa) {
        this.summa = summa;
    }
}
